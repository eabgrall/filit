#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	i;
	unsigned char	*b;

	b = s;
	i = 0;
	if (n != 0)
	{
		while (i < n)
		{
			b[i] = 0;
			i++;
		}
	}
}
